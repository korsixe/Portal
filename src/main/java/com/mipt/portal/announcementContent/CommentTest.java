package com.mipt.portal.announcementContent;


import java.sql.SQLException;

public class CommentTest {

  public static void main(String[] args) throws SQLException {
    // Создаем
    CommentManager manager = new CommentManager(1L, "Тестовый комментарий");
    Comment comment = manager.create();
    System.out.println("Создан комментарий ID: " + comment.getId());

    // Читаем
    Comment readComment = CommentManager.read(comment.getId());
    System.out.println("Прочитан: " + readComment.getText());

    // Обновляем
    CommentManager.update(comment.getId(), "Обновленный текст");
    System.out.println("Комментарий обновлен");

    // Удаляем
    CommentManager.delete(comment.getId());
    System.out.println("Комментарий удален");
  }
}
