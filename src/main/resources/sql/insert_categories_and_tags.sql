-- Связывание тегов с категориями товаров (не услуг)
INSERT INTO product_tags (category_id, tag_value_id)
SELECT c.id, tv.id
FROM categories c
         CROSS JOIN tag_values tv
WHERE c.is_service = false
  AND c.parent_id IS NOT NULL
  AND tv.id IN (
    SELECT tv2.id FROM tag_values tv2
                           JOIN tags t ON tv2.tag_id = t.id
    WHERE t.name IN ('Бренд', 'Цвет', 'Материал', 'Размер', 'Состояние', 'Гарантия')
)
    LIMIT 1000;